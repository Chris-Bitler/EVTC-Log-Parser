package data;

public class boonLog {

	// Fields
	private int time = 0;
	private int value = 0;

	// Constructor
	public boonLog(int time, int value) {
		this.time = time;
		this.value = value;
	}

	// Getters
	public int getTime() {
		return time;
	}

	public int getValue() {
		return value;
	}

}
