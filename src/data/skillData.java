package data;

public class skillData {

	// Fields
	private int ID;
	private String name;

	// Constructor
	public skillData(int ID, String name) {
		this.ID = ID;
		this.name = name;
	}

	// Getters
	public int getID() {
		return ID;
	}

	public String getName() {
		return name;
	}

}
